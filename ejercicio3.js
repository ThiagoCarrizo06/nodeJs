function suma(a , b) {
    return a + b;
}

function resta(a , b) {
    return a - b;
}

function multiplicacion(a , b) {
    return a * b;
}

function division(a , b) {
    return a / b;
}

console.log("suma: " + suma(2, 5));
console.log("resta: " + resta(9, 7));
console.log("division: " + division(10 , 2));
console.log("multiplicaion: " + multiplicacion(7 , 9));

